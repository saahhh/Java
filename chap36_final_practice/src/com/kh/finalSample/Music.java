package com.kh.finalSample;

public class Music {
	
	private String g;
	private String mood;

	public Music(String g, String mood) {
		this.g = g;
		this.mood = mood;
	}	
	
	public String getG() {
		return g;
	}
	public String getMood() {
		return mood;
	}
	
	public void setG(String g) {
		this.g = g;
	}
	public void setMood(String mood) {
		this.mood = mood;
	}

}
